func mustResolvePath(baseDir, p, title string) string {
	p = resolvePath(baseDir, p)
	if p == "" {
		log.Fatalf("lifebase configuration error: %s resolved to empty", title)
	}
	_, err := os.Stat(p)
	if err != nil {
		log.Fatalf("lifebase configuration error: %s does not exist", p)
	}
	return p
}

func resolvePath(baseDir, p string) string {
	p = strings.TrimSpace(p)
	if p == "" {
		return ""
	}
	if filepath.IsAbs(p) {
		return filepath.Clean(p)
	}
	return filepath.Clean(filepath.Join(baseDir, filepath.FromSlash(p)))
}

func promptPath(name string) string {
	name = strings.TrimSpace(name)
	if name == "" {
		return ""
	}
	if filepath.IsAbs(name) {
		return filepath.Clean(name)
	}
	rel := filepath.FromSlash(name)
	return filepath.Join(promptsDir, rel)
}
